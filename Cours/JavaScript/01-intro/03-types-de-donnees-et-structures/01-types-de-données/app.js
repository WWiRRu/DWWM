//* Types de données - 6 au total
//* Primitif - String, Number, Boolean, Null, Undefined, Symbol
//* non primitif - Arrays, Functions, Objects

//* typeof - opérateur qui retourne le type d'une valeur ou variable (typeof variable) (typeof valeur)

//* String - chaine de caractères
const text = "du texte";
console.log(text);
//* Number - nombre
const number = 45;
console.log(number);

//* Boolean - booléen (vrai ou faux)
const value1 = true;
const value2 = false;

//* Null
const result = null;

//* Undefined
let name;

//* Symbol (Es6)
const sym = Symbol();

console.log(typeof text);
console.log(typeof "hello word");
console.log(typeof number);
console.log(typeof value1);
console.log(typeof sym);
console.log(typeof result);
console.log(typeof name);